namespace Spice;

/// <summary>
/// Values used for horizontal and vertical alignment
/// </summary>
public enum Align
{
	/// <summary>
	/// Center this view along this axis
	/// </summary>
	Center,
	/// <summary>
	/// Stretch the view along this axis
	/// </summary>
	Stretch,
	/// <summary>
	/// Position the view at the beginning of the axis. Left or Top.
	/// </summary>
	Start,
	/// <summary>
	/// Position the view at the end of the axis. Right or Bottom.
	/// </summary>
	End,
}